##Write a Python program to demonstrate comparison operators: equal to, not equal to, greater than, less than.
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))
print(num1 == num2)
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)